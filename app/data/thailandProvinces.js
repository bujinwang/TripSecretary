/**
 * Get province display name by code
 * @param {string} code - Province code
 * @param {string} locale - Locale ('en', 'zh', 'th')
 * @returns {string} - Display name
 */
export const getProvinceDisplayName = (code, locale = 'en') => {
  const province = thailandProvinces.find(p => p.code === code);
  if (!province) return code;
  
  if (locale === 'zh') return `${province.name} - ${province.nameZh}`;
  if (locale === 'th') return province.nameTh;
  return province.name;
};

/**
 * Get province display name with both English and Chinese
 * @param {string} code - Province code
 * @returns {string} - Display name (English - Chinese)
 */
export const getProvinceDisplayNameBilingual = (code) => {
  const province = thailandProvinces.find(p => p.code === code);
  return province ? `${province.name} - ${province.nameZh}` : code;
};

/**
 * Get province code by display name
 * @param {string} displayName - Province display name (English)
 * @returns {string} - Province code
 */
export const getProvinceCode = (displayName) => {
  const province = thailandProvinces.find(p => p.name === displayName);
  return province ? province.code : displayName;
};
